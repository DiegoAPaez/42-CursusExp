#include "libft.h"
#include <unistd.h>

void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}
/*
* DESCRIPTION
*   The ft_putchar_fd() function writes the character c on the file 
*   descriptor fd.
*/
